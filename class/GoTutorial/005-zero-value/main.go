package main

import "fmt"

func main() {
	var b bool
	fmt.Println(b)
	fmt.Printf("b valueOF: %v, typeOf: %T\n",b,b)

	var x int
	fmt.Println(x)
	fmt.Printf("x valueOF: %v, typeOf: %T\n",x,x)

	var y string
	fmt.Println(y)
	fmt.Printf("y valueOF: '%v', typeOf: %T\n",y,y)

	var z float64
	fmt.Println(z)
	fmt.Printf("z valueOF: %v, typeOf: %T\n",z,z)



}